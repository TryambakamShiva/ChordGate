  This web application (chordgate.com) primarily utilizes combination of some work from the following GitHub repositories: <br>
1.	chord-extractor or https://github.com/ohollo/chord-extractor by ohollo ( https://github.com/ohollo ) which is Licensed under the GNU General Public License v2.0 and the web application uses the  Python library namely “chord-extractor” from the repository concerned to extract chord sequences from sound files of multiple formats. The extraction process wraps around Chordino ( https://code.soundsoftware.ac.uk/projects/nnls-chroma/ ). <br>

2.	ChordJS or https://github.com/acspike/ChordJS by acspike ( https://github.com/acspike ) which is Licensed under the MIT License and the web application uses the small javascript library Chords.js from the repository concerned to generate images of guitar chords in HTML. <br>

3.  html2canvas or https://github.com/niklasvh/html2canvas by niklasvh ( https://github.com/niklasvh ) which is Licensed under the MIT License and the web application uses library from the repository concerned to to take a screenshot of a specified HTML element and convert it into a canvas element. <br>

4.	jsPDF or https://github.com/parallax/jsPDF by parallax ( https://github.com/parallax ) which is Licensed under the MIT License and the web application uses library from the repository concerned to generate PDFs in JavaScript. <br><br>

The web-application is based on Python (3.8.2) using Flask framework. The website also uses AJAX-jQuery, PHP and MySQL for its different functionalities. <br>

Currently, an user can upload only MP3, WAV, AIFF and FLAC Audio (Music) Files (up to 25 MB) and the chords will be generated (extracted) from the music files (songs) and consequently generate images of the guitar chords (chord-diagram) in HTML. The extracted chords and chord-diagram can then be saved as .pdf file by users.  For best results, upload MP3 files. File Mime-Types are checked inside server for security protocols and so if one type of file is rejected based on MIME–type, please upload another file (suitably MP3). <br><br>

The concerned code for the entire web-application has been uploaded in this new GitHub repository ( https://github.com/TryambakamShiva/ChordGate ), 
with detailed discussion of pre-installation requisites, libraries etc. 
Please bear with the developer as the documentation is more improved. <br>
